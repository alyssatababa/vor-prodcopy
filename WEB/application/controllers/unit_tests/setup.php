	private function search_column_in_array($ar, $needle_value){
		$column_found = false;
		$data_type_match = false;
		$data_length_match = false;
		$data_default_match = false;
		//log_message('debug', 'needle : ' . $needle_value);
		$needle_array = explode(' ', $needle_value);
		$column_name = (isset($needle_array[0])) ? $needle_array[0] : null;
		$data_type = (isset($needle_array[1])) ? $needle_array[1] : null;
		$data_length = (isset($needle_array[2])) ? $needle_array[2] : null;
		$data_default = (isset($needle_array[3])) ? $needle_array[3] : null;
		
		$message =  'Column ' . $column_name . ' does not exists.';
	
		foreach($ar as $row) {
			foreach($row as $key=>$value) {
				// echo json_encode($value, JSON_PRETTY_PRINT);
				if (strtoupper($key) == strtoupper('COLUMN_NAME') ) {
					if (strtoupper($value)==strtoupper($column_name)) {
						$column_found = true;
						$message = '';
						if ($data_type==null || $row->DATA_TYPE==$data_type){
							$data_type_match = true;
						}else {
							$message .= 'Incorrect data type. Data type must be ' . $data_type;
						}
						// C = CHAR
						// B = BYTE
						if(@$row->CHAR_USED != null && $row->CHAR_USED == 'C'){			
							if ($data_length==null || $row->CHAR_LENGTH==$data_length){
								$data_length_match = true;
							}else{
								$message .= 'Incorrect data length. Data length must be ' . $data_length;
							}
						}else{								
							if ($data_length==null || $row->DATA_LENGTH==$data_length){
								$data_length_match = true;
							}else{
								$message .= 'Incorrect data length. Data length must be ' . $data_length;
							}
						}
						
						if ($data_default==null || $row->DATA_DEFAULT==$data_default){
							$data_default_match = true;
						}

						return array('result' =>  $data_type_match && $data_length_match, 'message' => $message);
						//return $data_type_match && $data_length_match; //&& $data_default_match;
					}
				}
			}
		}
		return array('result' => false, 'message' => $message);
	}

